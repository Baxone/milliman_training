numero_1 = 45
numero_2 = 12

# operadores de comparacion es que solo devuelve True o False.

# Mayor que o mayor o igual que
resultado = numero_1 > numero_2  # >=
print('mayor que:', resultado)  # True

# Menor que o menor o igual que
resultado = numero_1 < numero_2  # <=
print('menor que:', resultado)  # False

# Igual ==
resultado = numero_1 == numero_2  # False
print(resultado)

# Distinto !=
resultado = numero_1 != numero_2  # True
print(resultado)
