# Matematicos

numero_1 = 34
numero_2 = 2

# sumar +
print(numero_1 + numero_2)  # 36
# restar -
print(numero_1 - numero_2)  # 32
# multiplicar *
print(numero_1 * numero_2)  # 68
# division /
print(numero_1 / numero_2)  # 17 puede devolver float
# division entera //
print(17 // 3)
# modulo - resto de la division
print(17 % 3)  # 2

# operador incremento y decremento
numero_3 = 2
numero_3 += 4
numero_3 -= 2
print(numero_3)  # 4

# conversion de tipos numericos
numero_4 = "23.2"
# numero_entero = int(numero_4)  # 23
numero_racional = float(numero_4)  # 23.0
print(numero_racional)
numero_entero = int(float(numero_4))
print(numero_entero)
